int glyph_index(char ch) {
    if (ch == ' ') return 0;
    if (ch >= '0' && ch <= '9') return 1 + (ch - '0');
    if (ch >= 'A' && ch <= 'Z') return 11 + (ch - 'A');
    if (ch >= 'a' && ch <= 'z') return 11 + (ch - 'a');
    switch (ch) {
    case '/':  return 37; case '\\': return 38; case '-':  return 39;
    case ':':  return 40; case '@':  return 41; case '%':  return 42;
    case '#':  return 43; case '.':  return 44; case ',':  return 45;
    case '(':  return 46; case ')':  return 47; case '[':  return 48;
    case ']':  return 49; case '$':  return 50; case '_':  return 51;
    case '=':  return 52; case '*':  return 53; case '+':  return 54;
    case '!':  return 55; case '?':  return 56; case '>':  return 57;
    case '&':  return 58; case '\'': return 59; case '"':  return 60;
    case ';':  return 61; case '`':  return 62; case '~':  return 63;
    case '|':  return 64;
    default:   return 0;
    }
}
